package reto1;

import java.io.Serializable;

public class Coche implements Serializable {

	String marca;
	String modelo;
	String color;
	int ruedas;
	
	
	public Coche(String marca, String modelo, String color, int ruedas) {
		this.marca = marca;
		this.modelo = modelo;
		this.color = color;
		this.ruedas = ruedas;
	}
	
	
	
	
	
	
}
